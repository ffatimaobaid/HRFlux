def get_hr_procedure(query, keywords_only=False):
    """
    Find relevant HR procedure based on query keywords
    
    Args:
        query (str): User's query
        keywords_only (bool): Return only matching keywords if True
    
    Returns:
        dict: Matching procedure(s) and relevance score
    """
    query_lower = query.lower()
    matches = []
    
    for category, data in HR_KNOWLEDGE_BASE.items():
        keywords = data["keywords"]
        relevance_score = 0
        
        for keyword in keywords:
            if keyword in query_lower:
                relevance_score += 1
        
        if relevance_score > 0:
            if keywords_only:
                matches.append((category, keywords, relevance_score))
            else:
                matches.append((category, data["procedure"], relevance_score))
    
    # Sort by relevance score
    matches.sort(key=lambda x: x[2], reverse=True)
    
    return matches if matches else None

def get_all_hr_topics():
    """Return list of all available HR topics"""
    return list(HR_KNOWLEDGE_BASE.keys())

def format_hr_response(matches):
    """Format HR procedure matches into readable response"""
    if not matches:
        return None
    
    if len(matches) == 1:
        return matches[0][1]  # Return the procedure text
    
    # If multiple matches, return the most relevant
    return matches[0][1]
